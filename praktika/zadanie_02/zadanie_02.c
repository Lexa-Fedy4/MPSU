/*
 * Задание 2
 * Вывод чисел 0..10 на 2 семисегментных индикатора, PORTD.
 * Разряды PC0 (десятки), PC1 (единицы). Частота 10 МГц.
 * Среда: Atmel Studio (avr-gcc).
 */

#ifndef F_CPU                   /* если частота ещё не задана */
#define F_CPU 10000000UL              /* частота кварца 10 МГц */
#endif                              /* конец блока F_CPU */

#include <avr/io.h>                   /* регистры DDRx, PORTx, PINx */
#include <util/delay.h>               /* функция _delay_ms */

/* таблица кодов цифр 0..9 для общего катода (сегменты a..g на PORTD) */
const unsigned char seg_table[10] = {0x3F, 0x06, 0x5B, 0x4F, 0x66, 0x6D, 0x7D, 0x07, 0x7F, 0x6F};

void SSD_Show(unsigned char number)
{
    unsigned char tens;               /* цифра десятков */
    unsigned char ones;               /* цифра единиц */

    tens = number / 10;               /* вычислить десятки */
    ones = number % 10;               /* вычислить единицы */

    PORTD = seg_table[tens];          /* вывести код десятков на сегменты */
    PORTC |= (1 << PC1);              /* отключить разряд единиц (PC1 = 1) */
    PORTC &= ~(1 << PC0);             /* включить разряд десятков (PC0 = 0) */
    _delay_ms(2);                     /* пауза мультиплексирования 2 мс */
    PORTC |= (1 << PC0);              /* отключить разряд десятков */

    PORTD = seg_table[ones];          /* вывести код единиц на сегменты */
    PORTC |= (1 << PC0);              /* отключить разряд десятков (PC0 = 1) */
    PORTC &= ~(1 << PC1);             /* включить разряд единиц (PC1 = 0) */
    _delay_ms(2);                     /* пауза мультиплексирования 2 мс */
    PORTC |= (1 << PC1);              /* отключить разряд единиц */
}

int main(void)
{
    unsigned char value;              /* текущее число на индикаторе */

    value = 0;                        /* начать с 0 */
    DDRD = 0xFF;                      /* все выводы PORTD — выходы (сегменты) */
    DDRC |= (1 << PC0) | (1 << PC1);  /* PC0, PC1 — выходы (выбор разряда) */
    PORTC |= (1 << PC0) | (1 << PC1); /* оба разряда выключены (активный LOW) */

    while (1) {                       /* бесконечный цикл */
        SSD_Show(value);              /* показать текущее число */
        _delay_ms(300);               /* пауза перед сменой числа 300 мс */
        value++;                      /* следующее число */
        if (value > 10) {             /* если вышли за 10 */
            value = 0;                /* начать сначала */
        }
    }
}
