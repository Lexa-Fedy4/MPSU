/*
 * Задание 3 — LED PC6, PB5. Частота 10 МГц. Atmel Studio.
 */

#ifndef F_CPU                   /* если частота ещё не задана */
#define F_CPU 10000000UL              /* частота кварца 10 МГц */
#endif                              /* конец блока F_CPU */

#include <avr/io.h>                   /* регистры DDRx, PORTx, PINx */
#include <util/delay.h>               /* функция _delay_ms */

int main(void)
{
    unsigned int t1;                  /* счётчик времени LED1, мс */
    unsigned char s1;                 /* состояние автомата LED1 (0..3) */
    unsigned int t2;                  /* счётчик времени LED2, мс */
    unsigned char led2;               /* состояние LED2 (0/1) */

    t1 = 0;                           /* обнулить таймер LED1 */
    s1 = 0;                           /* начальное состояние LED1 */
    t2 = 0;                           /* обнулить таймер LED2 */
    led2 = 0;                         /* LED2 выключен */

    DDRC |= (1 << PC6);               /* PC6 — выход (LED1) */
    DDRB |= (1 << PB5);               /* PB5 — выход (LED2) */
    PORTC &= ~(1 << PC6);             /* LED1 погашен */
    PORTB &= ~(1 << PB5);             /* LED2 погашен */

    while (1) {                       /* бесконечный цикл */
        t1++;                         /* +1 мс к таймеру LED1 */
        switch (s1) {                 /* автомат LED1 */
            case 0:                   /* горит 10 с */
                PORTC |= (1 << PC6);  /* зажечь LED1 */
                if (t1 >= 10000) {    /* прошло 10 000 мс */
                    t1 = 0;           /* сброс таймера */
                    s1 = 1;           /* перейти к паузе */
                    PORTC &= ~(1 << PC6); /* погасить LED1 */
                }
                break;
            case 1:                   /* пауза 2 с */
                if (t1 >= 2000) {     /* прошло 2 000 мс */
                    t1 = 0;           /* сброс таймера */
                    s1 = 2;           /* перейти к второму включению */
                    PORTC |= (1 << PC6); /* зажечь LED1 */
                }
                break;
            case 2:                   /* горит 5 с */
                PORTC |= (1 << PC6);  /* LED1 включён */
                if (t1 >= 5000) {     /* прошло 5 000 мс */
                    t1 = 0;           /* сброс таймера */
                    s1 = 3;           /* перейти к повтору цикла */
                    PORTC &= ~(1 << PC6); /* погасить LED1 */
                }
                break;
            default:                  /* сброс цикла LED1 */
                t1 = 0;               /* обнулить таймер */
                s1 = 0;               /* вернуться в case 0 */
                break;
        }

        t2++;                         /* +1 мс к таймеру LED2 */
        if (t2 >= 3000) {             /* период LED2 — 3 с */
            t2 = 0;                   /* сброс таймера LED2 */
            led2 = !led2;             /* переключить LED2 */
            if (led2) {               /* включить LED2 */
                PORTB |= (1 << PB5);
            } else {                  /* выключить LED2 */
                PORTB &= ~(1 << PB5);
            }
        }
        _delay_ms(1);                 /* задержка 1 мс (шаг цикла) */
    }
}
