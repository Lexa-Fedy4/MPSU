/*
 * Задание 1 — PIC16F84A
 * RB6 — LED1, RB3 — LED2, RA2 — кнопка. Кварц 8 МГц.
 * Среда: microC PRO for PIC.
 */

void main()
{
    TRISA |= (1 << RA2);              /* RA2 — вход (кнопка) */
    PORTA |= (1 << RA2);              /* подтяжка RA2 к +5 В */

    TRISB |= 0xFF;                    /* все выводы PORTB — входы */
    TRISB &= ~((1 << RB6) | (1 << RB3)); /* RB6 и RB3 — выходы (LED) */

    OPTION_REG = 0;                   /* предделитель таймера TMR0 отключён */

    while (1) {                       /* основной цикл */
        if (PORTA & (1 << RA2)) {     /* кнопка не нажата (лог. 1 на RA2) */
            PORTB |= (1 << RB6);      /* зажечь LED1 на RB6 */
        } else {                      /* кнопка нажата */
            PORTB &= ~(1 << RB6);     /* погасить LED1 */
        }

        PORTB |= (1 << RB3);          /* зажечь LED2 на RB3 */
        Delay_ms(500);                /* пауза 500 мс */
        PORTB &= ~(1 << RB3);         /* погасить LED2 */
        Delay_ms(500);                /* пауза 500 мс */
    }
}
