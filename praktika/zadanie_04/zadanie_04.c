/*
 * Задание 4
 * Кнопка PC5, светодиод PB7. LED горит при нажатии. Частота 10 МГц.
 * Среда: Atmel Studio (avr-gcc).
 */

#ifndef F_CPU                   /* если частота ещё не задана */
#define F_CPU 10000000UL              /* частота кварца 10 МГц */
#endif                              /* конец блока F_CPU */

#include <avr/io.h>                   /* регистры DDRx, PORTx, PINx */
#include <util/delay.h>               /* функция _delay_ms */

int main(void)
{
    DDRC &= ~(1 << PC5);              /* PC5 — вход (кнопка) */
    PORTC |= (1 << PC5);              /* подтяжка PC5 к +5 В */

    DDRB |= (1 << PB7);               /* PB7 — выход (светодиод) */
    PORTB &= ~(1 << PB7);             /* LED погашен в начале */

    while (1) {                       /* бесконечный цикл */
        if (!(PINC & (1 << PC5))) {   /* кнопка нажата (лог. 0 на PC5) */
            PORTB |= (1 << PB7);      /* зажечь LED */
        } else {                      /* кнопка отпущена */
            PORTB &= ~(1 << PB7);     /* погасить LED */
        }
        _delay_ms(10);                /* пауза 10 мс (антидребезг) */
    }
}
