/*
 * Задание 20 — LED PB4, PD6. 8 МГц. Atmel Studio.
 */

#ifndef F_CPU                   /* если частота ещё не задана */
#define F_CPU 8000000UL              /* частота кварца 8 МГц */
#endif                              /* конец блока F_CPU */

#include <avr/io.h>                   /* регистры DDRx, PORTx, PINx */
#include <util/delay.h>               /* функция _delay_ms */

int main(void)
{
    unsigned int t1;                  /* счётчик времени LED1, мс */
    unsigned char on1;                /* флаг: LED1 должен гореть */
    unsigned int t2;                  /* счётчик времени LED2, мс */
    unsigned char phase2;             /* число переключений LED2 */
    unsigned char l2;                 /* состояние LED2 (0/1) */
    unsigned char perm;               /* LED2 зафиксирован включённым */

    t1 = 0;                           /* обнулить таймер LED1 */
    on1 = 1;                          /* LED1 начинает включённым */
    t2 = 0;                           /* обнулить таймер LED2 */
    phase2 = 0;                       /* обнулить счётчик фаз LED2 */
    l2 = 0;                           /* LED2 начинает выключенным */
    perm = 0;                         /* LED2 ещё мигает */

    DDRB |= (1 << PB4);       /* PB4 — выход (LED1) */
    DDRD |= (1 << PD6);       /* PD6 — выход (LED2) */
    PORTB &= ~(1 << PB4);     /* LED1 погашен на старте */
    PORTD &= ~(1 << PD6);     /* LED2 погашен на старте */

    while (1) {                       /* бесконечный цикл */
        t1++;                         /* +1 мс к таймеру LED1 */
        if (on1) {                    /* фаза включения LED1 */
            PORTB |= (1 << PB4); /* зажечь LED1 */
            if (t1 >= 500) {        /* прошло 500 мс */
                t1 = 0;               /* сброс таймера LED1 */
                on1 = 0;              /* перейти к паузе LED1 */
                PORTB &= ~(1 << PB4); /* погасить LED1 */
            }
        } else if (t1 >= 1000) {    /* пауза LED1 закончилась */
            t1 = 0;                   /* сброс таймера LED1 */
            on1 = 1;                  /* снова включать LED1 */
        }

        if (!perm) {                  /* LED2 ещё мигает */
            t2++;                     /* +1 мс к таймеру LED2 */
            if (t2 >= 1500) {    /* прошёл период LED2 */
                t2 = 0;               /* сброс таймера LED2 */
                l2 = !l2;             /* переключить состояние LED2 */
                if (l2) {             /* включить LED2 */
                    PORTD |= (1 << PD6);
                } else {              /* выключить LED2 */
                    PORTD &= ~(1 << PD6);
                }
                phase2++;             /* +1 переключение */
                if (phase2 >= 4) {    /* после 4 переключений */
                    perm = 1;         /* зафиксировать LED2 */
                    PORTD |= (1 << PD6); /* LED2 постоянно горит */
                }
            }
        }
        _delay_ms(1);                 /* задержка 1 мс (шаг цикла) */
    }
}