/*
 * Задание 17 — LED PB4, PD6. 8 МГц. Atmel Studio.
 */

#ifndef F_CPU                   /* если частота ещё не задана */
#define F_CPU 8000000UL               /* частота кварца 8 МГц */
#endif                              /* конец блока F_CPU */

#include <avr/io.h>                   /* регистры DDRx, PORTx, PINx */
#include <util/delay.h>               /* функция _delay_ms */

int main(void)
{
    unsigned int t1;                  /* счётчик времени LED1, мс */
    unsigned char s1;                 /* состояние автомата LED1 */
    unsigned int t2;                  /* счётчик времени LED2, мс */
    unsigned char l2;                 /* состояние LED2 (0/1) */

    t1 = 0;                           /* обнулить таймер LED1 */
    s1 = 0;                           /* начальное состояние LED1 */
    t2 = 0;                           /* обнулить таймер LED2 */
    l2 = 0;                           /* LED2 выключен */

    DDRB |= (1 << PB4);               /* PB4 — выход (LED1) */
    DDRD |= (1 << PD6);               /* PD6 — выход (LED2) */
    PORTB &= ~(1 << PB4);             /* LED1 погашен */
    PORTD &= ~(1 << PD6);             /* LED2 погашен */

    while (1) {                       /* бесконечный цикл */
        t1++;                         /* +1 мс к таймеру LED1 */
        switch (s1) {                 /* автомат LED1 */
            case 0:                   /* горит 1 с */
                PORTB |= (1 << PB4);  /* зажечь LED1 */
                if (t1 >= 1000) {     /* прошло 1 000 мс */
                    t1 = 0;           /* сброс таймера */
                    s1 = 1;           /* перейти к паузе */
                    PORTB &= ~(1 << PB4); /* погасить LED1 */
                }
                break;
            case 1:                   /* пауза 0.5 с */
                if (t1 >= 500) {      /* прошло 500 мс */
                    t1 = 0;           /* сброс таймера */
                    s1 = 2;           /* перейти к постоянному свечению */
                    PORTB |= (1 << PB4); /* зажечь LED1 */
                }
                break;
            default:                  /* LED1 постоянно горит */
                PORTB |= (1 << PB4);  /* удерживать LED1 включённым */
                break;
        }

        t2++;                         /* +1 мс к таймеру LED2 */
        if (t2 >= 500) {              /* период LED2 — 0.5 с */
            t2 = 0;                   /* сброс таймера LED2 */
            l2 = !l2;                 /* переключить LED2 */
            if (l2) {                 /* включить LED2 */
                PORTD |= (1 << PD6);
            } else {                  /* выключить LED2 */
                PORTD &= ~(1 << PD6);
            }
        }
        _delay_ms(1);                 /* задержка 1 мс (шаг цикла) */
    }
}
